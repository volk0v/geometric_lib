def area(a, b):
    '''
    Возвращает площадь прямоугольника.

        Параметры:
            a: длина стороны прямоугольника.
            b: длина стороны, перпендикулярной стороне прямоугольника с длиной 'a'.

        Возвращаемое значение:
            Площадь прямоугольника с длинами сторон 'a' и 'b'.
    '''
    return a * b

def perimeter(a, b):
    '''
    Возвращает периметр прямоугольника.

        Параметры:
            a: длина стороны прямоугольника.
            b: длина стороны, перпендикулярной стороне прямоугольника с длиной 'a'.

        Возвращаемое значение:
            Периметр прямоугольника с длинами сторон 'a' и 'b'.
    '''
    return 2 * (a + b)
